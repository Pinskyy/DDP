# Meminta input
bilangansatu = float(input("Masukkan bilangan pertama: "))
bilangandua = float(input("Masukkan bilangan kedua: "))
print()

# Penjumlahan/Pengurangan/Perkalian/Pembagian
Jumlah = bilangansatu + bilangandua
Kurang = bilangansatu - bilangandua
Kali = bilangansatu * bilangandua
Bagi = bilangansatu / bilangandua

# Menampilkan hasil penjumlahan
print("Hasil penjumlahan: ", Jumlah)

# Menampilkan hasil pengurangan
print("Hasil pengurangan: ", Kurang)

# Menampilkan hasil perkalian
print("Hasil perkalian: ", Kali)

# Menampilkan hasil pembagian
print("Hasil pembagian: ", Bagi, "\n")

